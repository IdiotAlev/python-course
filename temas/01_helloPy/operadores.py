#Operadores


#operadores aritmeticos
#suma
print(1+2)

#resta
print(2-1)

#multiplicacion
print(3*3)

#division
print(9/3)

#potencia
print(3**3)

#suelo
print(4//4) #Parte entera de una division

#modulo
print(3%4)#resto de una division


#Operadores relacionales
#devuelven true o false al comparar 2 datos
#mayor que >
print( 3>4)

#menor que <
print(8<10)

#igual igual que ==
print(4==4)

#moyor o igual que >=
print(8>=9)

#menor o igual que <=
print(7<=6)

#distinto que !=
print(3!=9)


#operadores logicos
#negacion Not
print(not True)
print(not False)

#disyuncion (Or)
#Las 2 tiene que ser falsas para dar false
print(False or False) #false
print(True or True)#true
print(False or True)#true

#Conjuncion (And)
#Las 2 tiene que ser true para dar true
print(True and True)
print(True and False)
print(False and False)


